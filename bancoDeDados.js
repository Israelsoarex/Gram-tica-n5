function contarPreenchidosEVazios(objeto) {
    let preenchidos = -1;
    let vazios = 0;

   
    for (let chave in objeto) {
        if (objeto[chave].sentence && objeto[chave].sentence.length > 0) {
            preenchidos++;
        } else {
            vazios++;
        }
    }

    
    return { preenchidos, vazios };
}


